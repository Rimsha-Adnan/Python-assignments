def main():
    # Anton's age is given
    anton = 21

    # Beth is 6 years older than Anton
    beth = anton + 6

    # Chen is 20 years older than Beth
    chen = beth + 20

    # Drew is as old as Chen's age plus Anton's age
    drew = chen + anton

    # Ethan is the same age as Chen
    ethan = chen

    # Printing all names and ages as required
    print("Anton is", anton)
    print("Beth is", beth)
    print("Chen is", chen)
    print("Drew is", drew)
    print("Ethan is", ethan)

# This line calls the main() function when the script is run
if __name__ == '__main__':
    main()
